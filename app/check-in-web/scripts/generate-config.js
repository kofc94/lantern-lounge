#!/usr/bin/env node
/**
 * Generates src/config/aws-config.js from OpenTofu outputs.
 * Run via: node scripts/generate-config.js
 * Or:      make generate-config
 */

import { execSync } from 'child_process';
import path from 'path';
import fs from 'fs';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const ROOT = path.resolve(__dirname, '../../..');

const ENV = process.argv[2] || 'prod';
console.log(`Generating config for environment: ${ENV}`);

function tfOutput(moduleDir, outputName) {
  const cwd = path.join(ROOT, moduleDir, 'infrastructure', ENV);
  try {
    return execSync(`tofu output -raw ${outputName}`, { cwd, stdio: ['pipe', 'pipe', 'pipe'] })
      .toString()
      .trim();
  } catch (e) {
    console.error(`❌ Failed to read output "${outputName}" from ${moduleDir}/infrastructure/${ENV}`);
    console.error(`   Make sure the module is deployed: cd ${moduleDir}/infrastructure/${ENV} && tofu apply`);
    process.exit(1);
  }
}

console.log('Reading OpenTofu outputs...');

const apiEndpoint        = tfOutput('app/calendar',  'api_gateway_endpoint');
const checkinsApiEndpoint = tfOutput('app/check-ins', 'api_gateway_endpoint');
const userPoolId         = tfOutput('app/cognito',   'cognito_user_pool_id');
const checkinAppClientId = tfOutput('app/cognito',   'checkin_app_client_id');
const cognitoDomain      = tfOutput('app/cognito',   'cognito_domain');

const content = `// AWS Cognito and API Configuration
// Auto-generated by scripts/generate-config.js — do not edit manually

const CONFIG = {
  // API Gateway endpoints
  apiEndpoint: import.meta.env.VITE_API_ENDPOINT ?? '${apiEndpoint}',
  checkinsApiEndpoint: import.meta.env.VITE_CHECKINS_API_ENDPOINT ?? '${checkinsApiEndpoint}',

  // Cognito User Pool Configuration
  cognito: {
    userPoolId: '${userPoolId}',
    userPoolRegion: 'us-east-1',
    appClientId: import.meta.env.VITE_COGNITO_CLIENT_ID ?? '${checkinAppClientId}',
    domain: '${cognitoDomain}.auth.us-east-1.amazoncognito.com'
  },

  // API Routes
  api: {
    getItems: '/calendar/items',
    createItem: '/calendar/items',
    updateItem: '/calendar/items',
    deleteItem: '/calendar/items',
    getPass: '/wallet/pass',
    checkIn: '/checkins',
    recordGuests: '/checkins/:id/guests'
  }
};

export default CONFIG;
`;

const outputPath = path.resolve(__dirname, '../src/config/aws-config.js');
fs.writeFileSync(outputPath, content);
console.log('✅ Generated src/config/aws-config.js');
